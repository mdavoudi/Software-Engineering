			<h1><br>Booking an Appointment</h1>

			<p>You can make an appointment in one of the following ways:</p>
			<ul>
				<li>Call <b>416-123-4567</b></li>
				<li>In person at the clinic</li>
				<li>Book online: Once you’ve created an account, you can use it to <a href="Register.php" target="_blank">book appointments</a> at any time. Just sign in to get started.</li>
			</ul>
			<h2>Types of Appointments</h2>
			<ol type="a">
				<li><b>Short Appointment (10 minutes):</b><br>
					A short appointment is appropriate for birth control refills, allergy injections, uncomplicated colds or bladder infections.  There will be time for one issue only. If you have other issues, you will need to book another appointment for it.  You can discuss with the doctor which issue is more urgent.</li>
				<li><b>Intermediate Appointment (20 minutes):</b><br>
					An intermediate appointment is recommended for more complicated medical issues such as chest pain, headaches, starting or switching contraceptive medication, sexually transmitted diseases  etc.  If you have several issues to discuss you may need to schedule more than one appointment in order to allow for time to address each issue.  It is best to discuss this with the doctor to determine what type of appointments you will need.</li>
				<li><b>Physical Examination:</b><br>
					Require two separate appointments, each 20 minutes.  The first appointment is to review medical concerns, past medical history, immunization and decide if any investigations need to be ordered.  The second appointment will involve a physical examination, review of results (if ordered) and Pap test for female patients if indicated by current guidelines.</li>
				<li><b>Mental Health Appointment (20 - 50 minutes):</b><br>
					Mental health appointments are appropriate if you are feeling sad, stressed, anxious, or having difficulty coping with school, work, home or relationships.  The first appointment is 30-50 minutes in length and will focus on getting a better understanding your situation as well as screening for common mental health disorders.  Follow up appointments are 20 minutes in length and will review symptoms and treatment.</li>
				<li><b>Prescription renewal:</b><br>
					We require an in-office visit, for medication renewals, so please call well in advance of running out of your medication.</li>
			</ol>
			<h2>Arrive early!</h2>
			<p>Plan to arrive at least 10 to 15 minutes before your appointment to allow for check-in and the completion of paperwork. Some appointments may require you to arrive 30 minutes prior to your appointment. You will be advised when you book the appointment.If you are late for an appointment, it may be cancelled and need to be rebooked for another time.</p>
			<h2>What to bring to your first appointment</h2>
			<ol>
				<li>Student Card</li>
				<li>MediCare or proof of other valid health insurance coverage</li>
				<li>Updated immunization records, TB testing information, and any information related to an ongoing condition</li>
			</ol>
